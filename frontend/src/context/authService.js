import axios from "axios";
import { LOGIN_PATH } from "../api/apiPaths";
import handleError from "../helpers/handleError";

async function loginAPI(userName, password) {
    try {
        const data = await axios.post(
            LOGIN_PATH,
            {
                userName: userName,
                password, password
            }
        );

        return data;   // MAP THIS TO TOKEN???????????????????????????????????????????????????????????????????????????????????????????????????????
    } 
    catch (error) {
        handleError(error);
    }
}